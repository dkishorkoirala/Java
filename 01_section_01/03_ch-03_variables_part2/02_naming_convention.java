
/*
 * Naming Conventions
 * 
 * 
 * In Java, it's important to follow naming conventions to keep your code
 * readable and maintainable. Here are some key rules:
 * 
 * Variable Names:
 * Use camelCase: Start with a lowercase letter, then capitalize the first
 * letter of each subsequent word (e.g., firstName, studentCount).
 * Choose descriptive names that indicate the variable's purpose (e.g., userAge
 * instead of ua).
 * Avoid single-letter names except for simple loop counters.
 * Constant Names:
 * Use UPPER_SNAKE_CASE: Write in uppercase letters, with words separated by
 * underscores (e.g., MAX_VALUE, PI_VALUE).
 * Use for values that don't change throughout the program.
 * General Rules:
 * Names can contain letters, digits, underscores, and dollar signs.
 * Names must start with a letter, an underscore _, or a dollar sign $.
 * Names are case-sensitive (myVariable is different from myvariable).
 * Avoid using Java's reserved keywords (like int, class, public, etc.).
 * Following these conventions helps make your code more understandable,
 * especially when working in teams or revisiting your code later.
 */