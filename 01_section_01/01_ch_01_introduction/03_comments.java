/*Comments


Comments are used to add explanatory notes within the code. They are ignored by the compiler and are meant for human understanding.

In Java, comments are written with special characters. There are two types of comments:

Single-line comments: Use // to comment out a single line.
Multi-line comments: Use /* to start and */ /*to end a multi-line comment block.
For example:

public class Main {
    public static void main(String[] args) {
        // This is a single-line comment
        System.out.println("Hello, Coddy!"); // This is another single-line comment
        
        /*
           This is a multi-line comment.
           It can span multiple lines.
         *//*
        System.out.println("Comments are useful for explaining code.");
    }
}
*/